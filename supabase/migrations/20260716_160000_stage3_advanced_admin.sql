CREATE OR REPLACE FUNCTION public.create_order_from_cart(
  p_user_id uuid,
  p_address_id bigint,
  p_cart_items jsonb, -- array of { book_id, quantity }
  p_payment_method text,
  p_discount_code text DEFAULT NULL,
  p_shipping_method_id bigint DEFAULT NULL
)
RETURNS jsonb AS $$
DECLARE
  v_address record;
  v_zone_id bigint;
  v_zone_name text;
  v_rate record;
  v_method record;
  v_shipping_fee numeric := 0;
  v_subtotal numeric := 0;
  v_discount_amount numeric := 0;
  v_shipping_discount_amount numeric := 0;
  v_total_amount numeric := 0;
  v_order_code text;
  v_order_id bigint;
  v_discount record;
  v_item record;
  v_book record;
BEGIN
  -- 1. Check Address & User
  SELECT * INTO v_address FROM public.shipping_addresses WHERE id = p_address_id AND user_id = p_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Address not found or does not belong to user'; END IF;

  -- 2. Determine Zone
  -- Tries to find matching province/district, falls back to 'TOAN_QUOC'
  SELECT z.id, z.name INTO v_zone_id, v_zone_name
  FROM public.shipping_zone_locations l
  JOIN public.shipping_zones z ON l.zone_id = z.id
  WHERE z.is_active = true 
    AND lower(l.province) = lower(v_address.province)
    AND (l.district IS NULL OR lower(l.district) = lower(v_address.district))
  ORDER BY z.priority DESC, l.district NULLS LAST
  LIMIT 1;

  IF v_zone_id IS NULL THEN
    SELECT id, name INTO v_zone_id, v_zone_name FROM public.shipping_zones WHERE code = 'TOAN_QUOC' AND is_active = true LIMIT 1;
    IF v_zone_id IS NULL THEN RAISE EXCEPTION 'No shipping zone found'; END IF;
  END IF;

  -- 3. Determine Shipping Rate
  IF p_shipping_method_id IS NOT NULL THEN
    SELECT m.id, m.code, m.name, m.min_delivery_days, m.max_delivery_days, m.supports_cod, r.base_fee, r.free_shipping_threshold
    INTO v_rate
    FROM public.shipping_rates r
    JOIN public.shipping_methods m ON r.method_id = m.id
    WHERE r.zone_id = v_zone_id AND m.id = p_shipping_method_id AND r.is_active = true AND m.is_active = true
      AND (r.starts_at IS NULL OR r.starts_at <= now())
      AND (r.expires_at IS NULL OR r.expires_at >= now())
    LIMIT 1;

    IF NOT FOUND THEN RAISE EXCEPTION 'Shipping method not available for this address'; END IF;
  ELSE
    -- Default to standard
    SELECT m.id, m.code, m.name, m.min_delivery_days, m.max_delivery_days, m.supports_cod, r.base_fee, r.free_shipping_threshold
    INTO v_rate
    FROM public.shipping_rates r
    JOIN public.shipping_methods m ON r.method_id = m.id
    WHERE r.zone_id = v_zone_id AND r.is_active = true AND m.is_active = true
      AND (r.starts_at IS NULL OR r.starts_at <= now())
      AND (r.expires_at IS NULL OR r.expires_at >= now())
    ORDER BY r.base_fee ASC LIMIT 1;

    IF NOT FOUND THEN RAISE EXCEPTION 'No shipping methods available for this address'; END IF;
  END IF;

  IF p_payment_method = 'cod' AND NOT v_rate.supports_cod THEN
    RAISE EXCEPTION 'Shipping method does not support COD';
  END IF;

  -- 4. Calculate subtotal & lock items
  FOR v_item IN SELECT * FROM jsonb_array_elements(p_cart_items)
  LOOP
    SELECT id, title, price, stock_quantity, is_active INTO v_book 
    FROM public.books WHERE id = (v_item.value->>'book_id')::bigint FOR UPDATE;

    IF NOT FOUND OR NOT v_book.is_active THEN RAISE EXCEPTION 'Product % is not available', v_item.value->>'book_id'; END IF;
    IF v_book.stock_quantity < (v_item.value->>'quantity')::integer THEN RAISE EXCEPTION 'Not enough stock for %', v_book.title; END IF;

    v_subtotal := v_subtotal + (v_book.price * (v_item.value->>'quantity')::integer);
  END LOOP;

  -- 5. Calculate Shipping Fee
  v_shipping_fee := v_rate.base_fee;
  IF v_rate.free_shipping_threshold IS NOT NULL AND v_subtotal >= v_rate.free_shipping_threshold THEN
    v_shipping_fee := 0;
  END IF;

  -- 6. Check Discount
  IF p_discount_code IS NOT NULL THEN
    SELECT * INTO v_discount FROM public.discount_codes 
    WHERE code = p_discount_code AND is_active = true 
      AND (starts_at IS NULL OR starts_at <= now())
      AND (expires_at IS NULL OR expires_at >= now())
    FOR UPDATE;

    IF FOUND THEN
      IF v_discount.usage_limit IS NOT NULL AND v_discount.current_usage >= v_discount.usage_limit THEN
        RAISE EXCEPTION 'Discount code usage limit reached';
      END IF;
      IF v_discount.min_order_amount IS NOT NULL AND v_subtotal < v_discount.min_order_amount THEN
        RAISE EXCEPTION 'Order amount does not meet discount minimum';
      END IF;

      IF v_discount.discount_type = 'percentage' THEN
        v_discount_amount := least(v_subtotal * (v_discount.discount_value / 100.0), COALESCE(v_discount.max_discount_amount, 999999999));
      ELSIF v_discount.discount_type = 'fixed_amount' THEN
        v_discount_amount := least(v_discount.discount_value, v_subtotal);
      ELSIF v_discount.discount_type = 'free_shipping' THEN
        v_shipping_discount_amount := v_shipping_fee;
        v_shipping_fee := 0;
      END IF;
    ELSE
      RAISE EXCEPTION 'Invalid discount code';
    END IF;
  END IF;

  v_total_amount := v_subtotal + v_shipping_fee - v_discount_amount;
  IF v_total_amount < 0 THEN v_total_amount := 0; END IF;

  v_order_code := 'ORD' || to_char(now(), 'YYYYMMDD') || lpad(floor(random() * 1000000)::text, 6, '0');

  -- 7. Create Order
  INSERT INTO public.orders (
    order_code, user_id, status, payment_status, payment_method, 
    subtotal_amount, shipping_fee, discount_amount, shipping_discount_amount, total_amount,
    shipping_name, shipping_phone, shipping_address_line, shipping_ward, shipping_district, shipping_province,
    shipping_method_id, shipping_method_code, shipping_method_name, shipping_zone_id, shipping_zone_name,
    estimated_delivery_min_days, estimated_delivery_max_days
  ) VALUES (
    v_order_code, p_user_id, 'pending', 'unpaid', p_payment_method,
    v_subtotal, v_shipping_fee, v_discount_amount, v_shipping_discount_amount, v_total_amount,
    v_address.full_name, v_address.phone, v_address.address_line, v_address.ward, v_address.district, v_address.province,
    v_rate.id, v_rate.code, v_rate.name, v_zone_id, v_zone_name,
    v_rate.min_delivery_days, v_rate.max_delivery_days
  ) RETURNING id INTO v_order_id;

  -- 8. Create Order Items & Deduct Stock
  FOR v_item IN SELECT * FROM jsonb_array_elements(p_cart_items)
  LOOP
    SELECT * INTO v_book FROM public.books WHERE id = (v_item.value->>'book_id')::bigint;
    
    INSERT INTO public.order_items (order_id, book_id, product_name, quantity, unit_price, line_total)
    VALUES (v_order_id, v_book.id, v_book.title, (v_item.value->>'quantity')::integer, v_book.price, v_book.price * (v_item.value->>'quantity')::integer);

    UPDATE public.books SET stock_quantity = stock_quantity - (v_item.value->>'quantity')::integer WHERE id = v_book.id;

    INSERT INTO public.inventory_movements (book_id, movement_type, quantity_change, quantity_after, reason)
    VALUES (v_book.id, 'out', -1 * (v_item.value->>'quantity')::integer, v_book.stock_quantity - (v_item.value->>'quantity')::integer, 'Order ' || v_order_code);
  END LOOP;

  -- 9. Handle Discount Redemption
  IF v_discount IS NOT NULL THEN
    UPDATE public.discount_codes SET current_usage = current_usage + 1 WHERE id = v_discount.id;
    INSERT INTO public.discount_code_redemptions (discount_code_id, user_id, order_id, discount_amount, shipping_discount_amount)
    VALUES (v_discount.id, p_user_id, v_order_id, v_discount_amount, v_shipping_discount_amount);
  END IF;

  -- 10. Update last order
  UPDATE public.profiles SET last_order_at = now() WHERE id = p_user_id;

  RETURN jsonb_build_object('success', true, 'order_id', v_order_id, 'order_code', v_order_code, 'total_amount', v_total_amount);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

