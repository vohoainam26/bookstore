CREATE OR REPLACE FUNCTION public.has_admin_permission(p_permission_code text)
RETURNS boolean AS $$
DECLARE
  v_uid uuid := auth.uid();
  v_has_permission boolean;
  v_is_super boolean;
BEGIN
  -- 1. Check if user is active
  IF NOT EXISTS (SELECT 1 FROM public.profiles WHERE id = v_uid AND is_active = true) THEN
    RETURN false;
  END IF;

  -- 2. Check if user is super_admin
  SELECT EXISTS (
    SELECT 1 FROM public.admin_user_roles ur
    JOIN public.admin_roles r ON ur.role_id = r.id
    WHERE ur.user_id = v_uid AND ur.is_active = true AND r.is_active = true AND upper(r.code) = 'SUPER_ADMIN'
  ) INTO v_is_super;

  IF v_is_super THEN RETURN true; END IF;

  -- 3. Check specific permission
  SELECT EXISTS (
    SELECT 1 FROM public.admin_user_roles ur
    JOIN public.admin_roles r ON ur.role_id = r.id
    JOIN public.admin_role_permissions rp ON r.id = rp.role_id
    JOIN public.admin_permissions p ON rp.permission_id = p.id
    WHERE ur.user_id = v_uid 
      AND ur.is_active = true 
      AND r.is_active = true 
      AND p.code = p_permission_code
  ) INTO v_has_permission;

  RETURN v_has_permission;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

CREATE OR REPLACE FUNCTION public.is_super_admin()
RETURNS boolean AS $$
BEGIN
  RETURN public.has_admin_permission('system.super_admin'); -- super admin logic is handled inside
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

