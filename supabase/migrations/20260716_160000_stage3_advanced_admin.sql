CREATE OR REPLACE FUNCTION public.admin_get_dashboard_analytics(p_start_date date, p_end_date date)
RETURNS jsonb AS $$
DECLARE
  v_revenue numeric;
  v_orders integer;
  v_delivered integer;
  v_customers integer;
BEGIN
  IF NOT public.has_admin_permission('analytics.view') THEN
    RAISE EXCEPTION 'Admin analytics permission required';
  END IF;

  SELECT coalesce(sum(total_amount), 0) INTO v_revenue
  FROM public.orders
  WHERE payment_status = 'paid' AND status != 'cancelled' 
    AND created_at >= p_start_date AND created_at < p_end_date + interval '1 day';

  SELECT count(id) INTO v_orders
  FROM public.orders
  WHERE status != 'cancelled' 
    AND created_at >= p_start_date AND created_at < p_end_date + interval '1 day';

  SELECT count(id) INTO v_delivered
  FROM public.orders
  WHERE status = 'delivered' 
    AND created_at >= p_start_date AND created_at < p_end_date + interval '1 day';

  SELECT count(distinct user_id) INTO v_customers
  FROM public.orders
  WHERE created_at >= p_start_date AND created_at < p_end_date + interval '1 day';

  RETURN jsonb_build_object(
    'revenue', v_revenue,
    'orders', v_orders,
    'delivered', v_delivered,
    'customers', v_customers
