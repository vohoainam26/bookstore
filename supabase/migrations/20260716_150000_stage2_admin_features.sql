CREATE OR REPLACE FUNCTION public.admin_update_product(
  p_product_id bigint,
  p_title text,
  p_price numeric,
  p_original_price numeric,
  p_category text,
  p_is_active boolean,
  p_is_featured boolean,
  p_low_stock_threshold integer,
  p_admin_note text
) RETURNS jsonb AS $$
DECLARE
  v_admin_id uuid;
  v_book record;
BEGIN
  v_admin_id := auth.uid();
  IF NOT public.is_admin() THEN
    RAISE EXCEPTION 'Admin access required';
  END IF;

  SELECT * INTO v_book FROM public.books WHERE id = p_product_id FOR UPDATE;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Product not found';
  END IF;

