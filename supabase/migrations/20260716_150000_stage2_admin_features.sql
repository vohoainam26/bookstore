CREATE OR REPLACE FUNCTION public.admin_set_discount_active(
  p_id bigint,
  p_is_active boolean
) RETURNS jsonb AS $$
DECLARE
  v_admin_id uuid;
  v_discount record;
BEGIN
  v_admin_id := auth.uid();
