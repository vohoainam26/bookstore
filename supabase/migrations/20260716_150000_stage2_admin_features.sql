CREATE OR REPLACE FUNCTION public.admin_adjust_inventory(
  p_book_id bigint,
  p_operation text,
  p_quantity integer,
  p_reason text
) RETURNS jsonb AS $$
DECLARE
  v_admin_id uuid;
  v_book record;
  v_quantity_after integer;
BEGIN
  v_admin_id := auth.uid();
  IF NOT public.is_admin() THEN RAISE EXCEPTION 'Admin access required'; END IF;
  
  IF p_quantity < 0 THEN RAISE EXCEPTION 'Quantity must be positive'; END IF;
  IF length(trim(p_reason)) < 3 THEN RAISE EXCEPTION 'Reason is required'; END IF;

  SELECT * INTO v_book FROM public.books WHERE id = p_book_id FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'Product not found'; END IF;

  IF p_operation = 'add' THEN
    v_quantity_after := v_book.stock_quantity + p_quantity;
  ELSIF p_operation = 'remove' THEN
    v_quantity_after := v_book.stock_quantity - p_quantity;
    IF v_quantity_after < 0 THEN RAISE EXCEPTION 'Cannot remove more stock than available'; END IF;
  ELSIF p_operation = 'set' THEN
    v_quantity_after := p_quantity;
  ELSE
    RAISE EXCEPTION 'Invalid operation';
  END IF;

  UPDATE public.books SET stock_quantity = v_quantity_after WHERE id = p_book_id;

  INSERT INTO public.inventory_movements (
    book_id, movement_type, quantity_change, quantity_before, quantity_after, reason, created_by
  ) VALUES (
    p_book_id, 'admin_' || p_operation,
    CASE WHEN p_operation = 'remove' THEN -p_quantity ELSE p_quantity END,
    v_book.stock_quantity, v_quantity_after, p_reason, v_admin_id
  );

  INSERT INTO public.admin_audit_logs (admin_id, action, entity_type, entity_id, new_data)
  VALUES (v_admin_id, 'inventory.' || p_operation, 'inventory', p_book_id::text, jsonb_build_object('before', v_book.stock_quantity, 'after', v_quantity_after));

  RETURN jsonb_build_object('success', true, 'quantity_after', v_quantity_after);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

REVOKE ALL ON FUNCTION public.admin_adjust_inventory(bigint, text, integer, text) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.admin_adjust_inventory(bigint, text, integer, text) TO authenticated;


