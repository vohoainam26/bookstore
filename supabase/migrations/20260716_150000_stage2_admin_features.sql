CREATE OR REPLACE FUNCTION public.create_order_from_cart(
  p_address_id bigint,
  p_items jsonb,
  p_payment_method text,
  p_customer_note text default null,
  p_coupon_code text default null
) RETURNS jsonb AS $$
DECLARE
  v_user_id uuid;
  v_address RECORD;
  v_order_id bigint;
  v_order_code text;
  v_subtotal numeric(14,2) := 0;
  v_shipping_fee numeric(14,2) := 30000;
  v_discount_amount numeric(14,2) := 0;
  v_shipping_discount_amount numeric(14,2) := 0;
  v_total_amount numeric(14,2) := 0;
  v_item record;
  v_book record;
  v_line_total numeric(14,2);
  v_order_status text := 'pending';
  v_payment_status text := 'unpaid';
  v_transfer_content text := null;
  
  v_coupon public.discount_codes;
  v_user_usage_count int;
  v_has_previous_order boolean;
  v_raw_discount numeric(14,2);
BEGIN
  v_user_id := auth.uid();
  IF v_user_id IS NULL THEN RAISE EXCEPTION 'Not authenticated'; END IF;

  IF p_payment_method NOT IN ('cod', 'bank_transfer_qr', 'test_payment') THEN RAISE EXCEPTION 'Invalid payment method'; END IF;

  SELECT * INTO v_address FROM public.shipping_addresses WHERE id = p_address_id AND user_id = v_user_id;
  IF NOT FOUND THEN RAISE EXCEPTION 'Shipping address not found or not owned by user'; END IF;

  v_order_code := 'FH-' || to_char(now(), 'YYMMDDHH24MISS') || '-' || upper(substring(md5(random()::text) from 1 for 4));

  FOR v_item IN SELECT * FROM jsonb_to_recordset(p_items) AS x(book_id bigint, quantity int) LOOP
    IF v_item.quantity <= 0 THEN RAISE EXCEPTION 'Quantity must be greater than 0'; END IF;

    -- Lock book for stock check
    SELECT * INTO v_book FROM public.books WHERE id = v_item.book_id FOR UPDATE;
    IF NOT FOUND THEN RAISE EXCEPTION 'Book with ID % not found', v_item.book_id; END IF;
    IF NOT v_book.is_active THEN RAISE EXCEPTION 'Book "%" is currently unavailable', v_book.title; END IF;
    IF v_book.stock_quantity < v_item.quantity THEN RAISE EXCEPTION 'Book "%" is out of stock (Requested: %, Available: %)', v_book.title, v_item.quantity, v_book.stock_quantity; END IF;
    IF v_book.price IS NULL THEN RAISE EXCEPTION 'Book with ID % has no price', v_item.book_id; END IF;

    v_line_total := v_book.price * v_item.quantity;
    v_subtotal := v_subtotal + v_line_total;

    -- Deduct Stock
    UPDATE public.books SET stock_quantity = stock_quantity - v_item.quantity WHERE id = v_item.book_id;
  END LOOP;

  IF v_subtotal >= 200000 THEN v_shipping_fee := 0; END IF;

  IF p_coupon_code IS NOT NULL AND trim(p_coupon_code) != '' THEN
      SELECT * INTO v_coupon FROM public.discount_codes WHERE upper(code) = upper(trim(p_coupon_code)) FOR UPDATE;
      IF NOT FOUND THEN RAISE EXCEPTION 'Mã giảm giá không tồn tại'; END IF;
      IF NOT v_coupon.is_active THEN RAISE EXCEPTION 'Mã giảm giá đã bị khoá'; END IF;
      IF v_coupon.starts_at IS NOT NULL AND now() < v_coupon.starts_at THEN RAISE EXCEPTION 'Mã giảm giá chưa bắt đầu'; END IF;
      IF v_coupon.expires_at IS NOT NULL AND now() > v_coupon.expires_at THEN RAISE EXCEPTION 'Mã giảm giá đã hết hạn'; END IF;
      IF v_subtotal < v_coupon.min_order_amount THEN RAISE EXCEPTION 'Đơn hàng chưa đạt giá trị tối thiểu'; END IF;
      IF v_coupon.usage_limit IS NOT NULL AND v_coupon.current_usage >= v_coupon.usage_limit THEN RAISE EXCEPTION 'Mã giảm giá đã hết lượt sử dụng'; END IF;
      
      SELECT count(*) INTO v_user_usage_count FROM public.discount_code_redemptions WHERE discount_code_id = v_coupon.id AND user_id = v_user_id AND status != 'released';
      IF v_user_usage_count >= v_coupon.usage_limit_per_user THEN RAISE EXCEPTION 'Bạn đã sử dụng hết lượt cho mã này'; END IF;

      IF v_coupon.first_order_only THEN
          SELECT EXISTS(SELECT 1 FROM public.orders WHERE user_id = v_user_id AND status NOT IN ('cancelled')) INTO v_has_previous_order;
          IF v_has_previous_order THEN RAISE EXCEPTION 'Mã chỉ áp dụng cho đơn hàng đầu tiên'; END IF;
      END IF;

      IF v_coupon.discount_type = 'percentage' THEN
          v_raw_discount := v_subtotal * (v_coupon.discount_value / 100.0);
          IF v_coupon.max_discount_amount IS NOT NULL THEN
              v_discount_amount := least(v_raw_discount, v_coupon.max_discount_amount);
          ELSE
              v_discount_amount := v_raw_discount;
          END IF;
      ELSIF v_coupon.discount_type = 'fixed_amount' THEN
          v_discount_amount := least(v_coupon.discount_value, v_subtotal);
      ELSIF v_coupon.discount_type = 'free_shipping' THEN
          v_discount_amount := 0;
          v_shipping_discount_amount := least(v_shipping_fee, v_coupon.discount_value);
      END IF;

      UPDATE public.discount_codes SET current_usage = current_usage + 1 WHERE id = v_coupon.id;
  END IF;

  v_total_amount := v_subtotal + v_shipping_fee - v_discount_amount - v_shipping_discount_amount;
  IF v_total_amount < 0 THEN v_total_amount := 0; END IF;

  IF p_payment_method = 'bank_transfer_qr' THEN v_order_status := 'pending_payment'; v_payment_status := 'awaiting_payment'; v_transfer_content := v_order_code;
  ELSIF p_payment_method = 'cod' THEN v_order_status := 'pending'; v_payment_status := 'unpaid';
  ELSIF p_payment_method = 'test_payment' THEN v_order_status := 'pending_payment'; v_payment_status := 'awaiting_payment'; v_transfer_content := v_order_code; END IF;

  INSERT INTO public.orders (
    order_code, user_id, address_id, status, payment_status, payment_method,
    subtotal, shipping_fee, discount_amount, shipping_discount_amount, total_amount, shipping_address_snapshot, customer_note,
    discount_code_id, discount_code_snapshot, discount_type_snapshot, discount_value_snapshot
  )
  VALUES (
    v_order_code, v_user_id, p_address_id, v_order_status, v_payment_status, p_payment_method,
    v_subtotal, v_shipping_fee, v_discount_amount, v_shipping_discount_amount, v_total_amount,
    jsonb_build_object('recipient_name', v_address.recipient_name, 'phone', v_address.phone, 'address_line', v_address.address_line, 'ward', v_address.ward, 'district', v_address.district, 'province', v_address.province, 'postal_code', v_address.postal_code, 'delivery_note', v_address.delivery_note),
    p_customer_note, (CASE WHEN v_coupon.id IS NOT NULL THEN v_coupon.id ELSE null END), (CASE WHEN v_coupon.id IS NOT NULL THEN v_coupon.code ELSE null END), (CASE WHEN v_coupon.id IS NOT NULL THEN v_coupon.discount_type ELSE null END), (CASE WHEN v_coupon.id IS NOT NULL THEN v_coupon.discount_value ELSE null END)
  )
  RETURNING id INTO v_order_id;

  IF v_coupon.id IS NOT NULL THEN
      INSERT INTO public.discount_code_redemptions (discount_code_id, user_id, order_id, status, discount_amount, shipping_discount_amount) VALUES (v_coupon.id, v_user_id, v_order_id, 'reserved', v_discount_amount, v_shipping_discount_amount);
  END IF;

  FOR v_item IN SELECT * FROM jsonb_to_recordset(p_items) AS x(book_id bigint, quantity int) LOOP
    SELECT * INTO v_book FROM public.books WHERE id = v_item.book_id;
    v_line_total := v_book.price * v_item.quantity;

    INSERT INTO public.order_items (order_id, book_id, source_id, product_title, product_slug, product_image_url, unit_price, original_price, quantity, line_total)
    VALUES (v_order_id, v_book.id, v_book.source_id, v_book.title, v_book.slug, v_book.image_url, v_book.price, v_book.original_price, v_item.quantity, v_line_total);
    
    INSERT INTO public.inventory_movements (book_id, movement_type, quantity_change, quantity_before, quantity_after, reason, reference_type, reference_id, created_by)
    VALUES (v_book.id, 'order_created', -v_item.quantity, v_book.stock_quantity + v_item.quantity, v_book.stock_quantity, 'Order placed', 'order', v_order_id::text, v_user_id);
  END LOOP;

  IF p_payment_method IN ('bank_transfer_qr', 'test_payment') THEN
     INSERT INTO public.payment_transactions (order_id, user_id, payment_method, amount, status, transfer_content) VALUES (v_order_id, v_user_id, p_payment_method, v_total_amount, 'pending', v_transfer_content);
  END IF;

  RETURN jsonb_build_object('order_id', v_order_id, 'order_code', v_order_code, 'total_amount', v_total_amount, 'payment_method', p_payment_method, 'payment_status', v_payment_status, 'status', v_order_status, 'transfer_content', v_transfer_content);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

