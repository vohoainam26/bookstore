  END IF;

  SELECT * INTO v_review FROM public.product_reviews WHERE id = p_review_id FOR UPDATE;
  IF NOT FOUND THEN RAISE EXCEPTION 'Review not found'; END IF;

  UPDATE public.product_reviews
  SET status = p_new_status, moderation_reason = p_reason, moderated_by = v_admin_id, moderated_at = now()
  WHERE id = p_review_id;

  INSERT INTO public.admin_audit_logs (admin_id, action, entity_type, entity_id, new_data)
  VALUES (v_admin_id, 'review.' || p_new_status, 'review', p_review_id::text, jsonb_build_object('reason', p_reason));

  RETURN jsonb_build_object('success', true);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

