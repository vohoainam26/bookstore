-- 2. INVENTORY MOVEMENTS
-- ==============================================================================
CREATE TABLE IF NOT EXISTS public.inventory_movements (
  id bigint generated by default as identity primary key,
  book_id bigint not null references public.books(id) on delete restrict,
  movement_type text not null,
  quantity_change integer not null,
  quantity_before integer not null,
  quantity_after integer not null,
  reason text not null,
  reference_type text,
  reference_id text,
  created_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),

  constraint inventory_movements_type_check
    check (
      movement_type in (
        'admin_add',
        'admin_remove',
        'admin_set',
        'order_created',
        'order_cancelled',
        'order_returned',
        'manual_correction'
      )
    ),

  constraint inventory_movements_result_check
    check (
      quantity_before >= 0
      and quantity_after >= 0
    )
);

CREATE INDEX IF NOT EXISTS inventory_movements_book_id_idx ON public.inventory_movements(book_id, created_at desc);
CREATE INDEX IF NOT EXISTS inventory_movements_created_at_idx ON public.inventory_movements(created_at desc);
CREATE INDEX IF NOT EXISTS inventory_movements_created_by_idx ON public.inventory_movements(created_by);

ALTER TABLE public.inventory_movements ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Admins can view inventory movements"
ON public.inventory_movements FOR SELECT
TO authenticated USING (public.is_admin());


